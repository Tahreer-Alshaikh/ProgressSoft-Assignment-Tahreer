#!/bin/bash
# system_info.sh
# script to display all OS information 

echo -e "\e[1m System Information\e[0m"
echo "  "
#--------------------------------------------------------------------------
echo "Executed_by: $(whoami)"
echo "  "
#---------------------------------------------------------------------------
echo "Hostname: $(hostname)"
echo "  "
#----------------------------------------------------------------------------
echo "Server IP: $(hostname -I | awk '{print $1}')"
echo "  "
#-----------------------------------------------------------------------------
echo "Public IP: $(curl -s ifconfig.me || echo 'unavailable')"
echo "  "
#-------------------------------------------------------------------------------
source /etc/os-release
echo "OS: $PRETTY_NAME"
echo "  "
#--------------------------------------------------------------------------------
echo "Kernel: $(uname -r)"
echo "  "
#--------------------------------------------------------------------------------
echo "Architecture: $(uname -m)"
echo "  "
#--------------------------------------------------------------------------------
echo "Virtualization: $(lscpu | grep 'Hypervisor vendor' | awk '{print $3}' || echo 'unknown')"
echo "  "
#--------------------------------------------------------------------------------
echo "Time and Date: $(date)"
echo "  "
#--------------------------------------------------------------------------------
echo "Timezone: $(date +'%Z (%Z)')"
echo "  "
#--------------------------------------------------------------------------------
echo "Uptime: $(uptime -p)"
echo "  "
#--------------------------------------------------------------------------------
#-------------------------------------------------------------------------------
echo  -e "\e[1m Resource Usage\e[0m"
echo "  "
#--------------------------------------------------------------------------------
echo "Total Memory: $(free -h | awk '/Mem:/ {print $2}')"
echo "    "
#-------------------------------------------------------------------------------

echo "Memory: $(free -h | grep Mem | awk '{print $3 "/" $2}')"
echo "  "
#---------------------------------------------------------------------------------
echo "Swap: $(free -h | grep Swap | awk '{print $3 "/" $2}')"
echo "  "
#--------------------------------------------------------------------------------
echo "CPU Cores: $(nproc)"
echo "   "
